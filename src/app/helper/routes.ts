/**
 * Enumeration defining route paths used within the application.
 */
export enum Routes {
  /**
   * Root path.
   */
  Root = "",
  /**
   * Path for the login page.
   */
  Login = '/login',
  /**
   * Path for the tabs page.
   */
  Tabs = '/tabs',
  /**
   * Path for the home page.
   */
  Home = '/home',
  /**
   * Path for the settings page.
   */
  Settings = '/settings',
  /**
   * Path for the detail page.
   */
  Detail = "/detail"
}
